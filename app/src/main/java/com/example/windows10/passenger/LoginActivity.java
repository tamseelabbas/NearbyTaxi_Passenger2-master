    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Void, Void, Boolean> {

        private final String mEmail;
        private final String mPassword;
        private  String mName;
        private  String mAge;
        private  String mContactNumber;
        private  String mCnic;


        UserLoginTask(String email, String password) {
            mEmail = email;
            mPassword = password;
        }
        UserLoginTask(String email, String password,String name,String age,String contactNumber,String cnic) {
            mEmail = email;
            mPassword = password;
            mName=name;
            mAge=age;
            mContactNumber=contactNumber;
            mCnic=cnic;

        }

        @Override
        protected Boolean doInBackground(Void... params) {
            // TODO: attempt authentication against a network service.

            try {
                // Simulate network access.
                Thread.sleep(1000);
            } catch (InterruptedException e) {
                return false;
            }



            if(checkSignInOrSignUp==1) {
                mAuth.createUserWithEmailAndPassword(mEmail, mPassword)
                        .addOnCompleteListener(LoginActivity.this, new OnCompleteListener<AuthResult>() {
                            @Override
                            public void onComplete(@NonNull Task<AuthResult> task) {


                                // If sign in fails, display a message to the user. If sign in succeeds
                                // the auth state listener will be notified and logic to handle the
                                // signed in user can be handled in the listener.
                                if (!task.isSuccessful()) {
                                    showProgress(false);
                                    mPasswordView.setError(getString(R.string.error_incorrect_password));
                                    mPasswordView.requestFocus();
                                } else {

                                    String key = mAuth.getCurrentUser().getUid();
                                    Passenger p1 = new Passenger(key, mName, mContactNumber,Integer.parseInt(mAge),mCnic, 0, 0, 0, "", -1,0,0);
                                    Toast.makeText(LoginActivity.this, "success",
                                            Toast.LENGTH_SHORT).show();

                                    FirebaseDatabase database = FirebaseDatabase.getInstance();
                                    DatabaseReference myRef = database.getReference("users/passenger/");
                                    myRef.child(key).setValue(p1);
                                }

                                // ...
                            }
                        });

            }
            else {
                mAuth.signInWithEmailAndPassword(mEmail, mPassword)
                        .addOnCompleteListener(LoginActivity.this, new OnCompleteListener<AuthResult>() {
                            @Override
                            public void onComplete(@NonNull Task<AuthResult> task) {


                                // If sign in fails, display a message to the user. If sign in succeeds
                                // the auth state listener will be notified and logic to handle the
                                // signed in user can be handled in the listener.
                                if (!task.isSuccessful()) {

                                    showProgress(false);
                                    mPasswordView.setError(getString(R.string.error_incorrect_password));
                                    mPasswordView.requestFocus();


                                }
                                // ...
                            }
                        });
            }

            return true;

        }

        @Override
        protected void onPostExecute(final Boolean success) {
            mAuthTask = null;
            showProgress(true);


        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }
    }
